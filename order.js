const price = [25, 15, 30, 50,45,20, 3]

let indexMenorValor = 0

for (let i = 0; i < price.length; i++) {
  if (price[i] > price[indexMenorValor]) {
    indexMenorValor = i
  }
}
console.log(`o menor valor e =`, price[indexMenorValor])

